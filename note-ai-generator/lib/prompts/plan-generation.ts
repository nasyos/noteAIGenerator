import { Topic } from '@/types/database';

export function generatePlanPrompt(topic: Topic): string {
  return `
【AI業務改善note記事企画書作成】

あなたはEXTOOL株式会社の代表として、企業向けの実践的なAI活用記事の企画を作成します。

## トピック情報
- トピック: ${topic.topic_name}
- カテゴリ: ${topic.category}
- キーワード: ${topic.keywords}
- 想定読者: ${topic.target_audience}
- 切り口: ${topic.angle}

## 企画要件

### タイトル案（3パターン）
以下の観点で異なる3つのタイトルを提案してください：
1. 数字を使った具体的なタイトル（例: 「5つの方法」「3ステップ」）
2. 問題提起型のタイトル（例: 「〜で困っていませんか？」）
3. ベネフィット訴求型のタイトル（例: 「〜で業務時間を30%削減」）

各タイトルは25-35字、読者の関心を引く工夫を。

### リード文
- 120-150字
- 読者の課題に共感
- 記事で得られる価値を明確に
- 続きを読みたくなる引き

### 記事構成（章立て）
以下の構成で5-7章を提案：
1. 導入（現状の課題・背景）
2-3. 課題の深掘り・具体例
4-5. 解決策・アプローチ（具体的に）
6. 実装ステップ
7. まとめ・次のアクション

各章について：
- 見出し（15-25字）
- 要点（100-150字）
- 推定文字数

## 出力形式
以下のJSON形式で出力してください：

{
  "titleOptions": [
    "タイトル案1",
    "タイトル案2",
    "タイトル案3"
  ],
  "lead": "リード文",
  "structure": [
    {
      "heading": "章の見出し",
      "content": "この章で書くべき要点・ポイント",
      "estimatedLength": 推定文字数
    }
  ],
  "targetLength": 全体の目標文字数（2500-3000）
}

必ずJSON形式のみで出力してください。他の説明は不要です。
  `.trim();
}
